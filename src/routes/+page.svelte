<h2>About Great kingdom</h2>
Game created by Lee Sedol, a former Go player who won a game against AlphaGo.
<h2>Rules</h2>
<p>The 'Neutral Castle' is placed at the middle of the board.</p>
<p>Blue player plays first.</p>
<p>Each turn, player can do:</p>
<ul>
	<li>Place a castle.</li>
	<li>Do nothing and pass the turn.</li>
</ul>
<p>
	If (player's castles/3 or less edges/neutral castle) surrounds several spaces, all of them become
	player's territories.
</p>
<p>If the space contains opponent's castle, the space won't be captured.</p>
<p>Castle cannot be placed within the territory.</p>
<p>If a player surround opponent's castle compactly, the player win the game.</p>
<p>
	If both player passed their turn in a row, or if there's no space to put castle, the game will
	count each player's territory.
</p>
<p>To defeat orange player, blue player must capture 3 or more territories than orange player.</p>
