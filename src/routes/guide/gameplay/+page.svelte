<h2>게임 규칙</h2>

<h3>기본 규칙</h3>
<p>
	게임을 시작하면 난수 굴림을 통해 선후공이 정해집니다. 선공은 파란색, 후공은 주황색 성을
	사용합니다. 선공 플레이어부터 하나씩 보드 위에 성을 배치합니다. 성을 배치하지 않고 패스를 선언할
	수도 있습니다.
</p>
<h3>영토</h3>
<p>
	보드 위의 빈 공간을 (자신의 성 / 중립 성 / 가장자리) 로 둘러싸게 되면 둘러싸인 공간은 자신의
	영토가 됩니다. 둘러싼 공간은 모두 빈 공간이어야 하며, 둘러싼 공간 내에 상대의 성이 있거나 둘러싼
	공간이 4개의 가장자리와 닿아 있다면 그 공간은 영토가 되지 않습니다. 상대의 영토에는 성을 놓을 수
	없습니다. 두 플레이어가 연속으로 패스를 선언하거나 보드 위에 더 이상 성을 배치할 수 없다면 게임이
	종료되고, 서로가 가진 영토를 세어 승패를 결정합니다. 선공은 후공보다 영토가 세 개 이상 많아야
	승리합니다(덤 2.5로 계산). 영토는 보드 위에서 어두운 파란색/어두운 주황색으로 표시됩니다.
</p>
<h3>파괴</h3>
<p>
	상대 플레이어의 성을 (자신의 성 / 중립 성 / 가장자리) 로 빈 공간 없이 완전히 둘러싸게 되면
	둘러싸인 상대의 성을 파괴하여 게임이 종료됩니다. 파괴된 성은 붉은색으로 표시됩니다.
</p>
