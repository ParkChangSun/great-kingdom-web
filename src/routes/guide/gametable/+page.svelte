<h2>게임 테이블 UI</h2>

<h3>게임 보드</h3>
<p>
	플레이어들은 자신의 턴에 보드 위를 클릭해 그 지역에 성을 놓을 수 있습니다. 이전 턴의 행마는
	초록색으로 표시됩니다.
</p>
<p>
	보드 오른쪽에는 패스 버튼과 항복 버튼이 있습니다. 상대가 패스했다면 패스 버튼이 초록색으로
	표시됩니다.
</p>

<h3>턴 및 제한시간</h3>
<p>
	각 플레이어의 턴에 플레이어의 색으로 강조됩니다. 각 플레이어가 가진 시간은 5분이며 성을 놓을
	때마다 1초씩 추가됩니다.
</p>

<h3>테이블 관리</h3>
<p>
	방장(👑)은 게임을 시작할 수 있는 권한이 있습니다. 게임 시작 버튼은 상대 플레이어가 잠수 상태일 때
	강제 퇴장시키는 버튼으로 바뀝니다.
</p>
<p>관전자로 이동 버튼을 누르면 관전자와 플레이어를 이동할 수 있습니다.</p>
